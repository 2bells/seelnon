        // Label
        ctx.font = NODE_LABEL_FONT;
        ctx.fillStyle = hover || isActive ? NODE_ACTIVE_COLOR : NODE_INACTIVE_COLOR;
        ctx.textAlign = 'center';
        ctx.textBaseline = 'bottom'; // Place label above the @ symbol
